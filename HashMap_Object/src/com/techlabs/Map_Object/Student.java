package com.techlabs.Map_Object;

public class Student {
	private String name;
	private int id;

	public Student(String name, int id) {
		this.name = name;
		this.id = id;
	}

	public String getName() {
		return this.name;
	}

	public int getId() {
		return this.id;
	}
	
	public void setName(String name)
	{
		this.name=name;
	}



}
